namespace AudioEffectKit
{
    public enum AudioCategory
    {
        SFX = 0,        // 音效：UI点击、物体碰撞等短音效
        Music = 1,      // 音乐：背景音乐、主题曲等长音频
        Voice = 2,      // 语音：角色对话、旁白等人声
        UI = 3,         // 界面音效：按钮点击、菜单切换等
        Ambient = 4,    // 环境音：风声、水声、鸟叫等
        Combat = 5      // 战斗音效：武器声、爆炸声、技能音效等
    }
}